public class IntegerValue {
  public static void main(String[] argv) {
    System.out.println("10턨 1357       = " + 1357);
  
    int i = 0b01100110; // int ガ쬜, 2턨 
    System.out.println("2턨  0b10010110 = " + i);
    
    long l = 0XABCD; // long ガ쬜, 16턨 
    System.out.println("16턨 0XABCD     = " + l);
    
    short s = 07654; // short ガ쬜, 8턨
    System.out.println("8턨  07654      = " + s);
  }
}
